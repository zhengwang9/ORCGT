def get_batch_data(train_data, batch_size):
    data_list = []
    label_list = []
    for data_tuple in train_data:
        data,label = data_tuple
        data_list.append(data)
        label_list.append(label)
    labels = torch.tensor(label_list)
    
    # 将全部数据再按batch封装
    
    step = batch_size
    data_array_temp = [data_list[i:i+step] for i in range(0,len(data_list),step)]
    label_array_temp = [labels[i:i+step] for i in range(0,len(labels),step)]
    return data_array_temp,label_array_temp